                <h2 class="mb-3"><b>Keyword Research</b></h2>
                <p>The whole point of SEO is to improve your keyword rankings, so it should be obvious that keyword research is an essential part of a successful SEO strategy. Yet, it is an often overlooked part of the equation.</p>
                <p>In organic search, a keyword refers to any word or phrase that is typed into Google and searched. A keyword can be one word, like “SEO”, or many words, like “how to choose a SEO company”. Keywords can be incomplete thought fragments like “open now pizza near me delivery” or questions like “where can I buy pizza and have it delivered to me right now?” Anything that has been (or ever will be) searched in Google is a keyword.</p>
                <p>Keyword research is the process of discovering which search terms people enter into a search engine to find a given product, service, or piece of information. It also includes digging deeper to find ancillary keywords that people might use before they are even aware of their need for that product or service. Using various keyword tools (Google Keyword Planner, Ahrefs.com, SEMRush, etc.), SEO professionals have access to lots of data about any given keyword which helps us make informed decisions on the best keywords to target for a given website or page.</p>
                <p>Keyword researchers must also explore the many nuances of how people use language differently, as well as how language use and lexicon vary from region to region. Digital personal assistants like Amazon Alexa and Google Home are fantastic examples of how quickly a shift can happen in the way people search. Since many people are searching Google by voice instead of typing, SEO professionals have observed a clear trend towards the long tail and query-based keywords.</p>
                <p>Keyword Research goes hand-in-hand with many other services on this list. For example, keyword research is the foundation of an effective content marketing strategy, as you develop content based on what you know people are already searching for on Google. Researching and targeting the right keywords for your site will help you to create rich and targeted content that drives QUALIFIED traffic to your site that is more likely to complete your desired actions once they arrive.</p>
