                <h2 class="mb-3"><b>URL Redirection (301s, etc.)</b></h2>
                <p>Launching a brand new website with hundreds or thousands of new URLs? For usability and SEO reasons, it is critical that you redirect the old URL to the corresponding new page. URL redirects ensure that any links on the web that direct to your old pages get properly forwarded to the most relevant page so that you don’t lose out on any potential conversions.</p>
                <p>More importantly, if the old pages on your website had links pointing to them, 301 redirects are necessary to preserve your “link juice” and ensure your historical links continue to work in your favor. An SEO company will perform a backlink profile audit and look into your website’s analytics to develop a redirection strategy that will ensure your new website launches with its best foot forward and is ready to hit the ground running. URL redirection is one of the most overlooked aspects when it comes to redesigning a website, and can have catastrophic effects on your website’s organic (and referral) traffic if not handled with extreme care.</p>
